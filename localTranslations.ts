export function getLocalTranslation(lang: string, text: string): string | undefined {
  const l = (lang || "en").toLowerCase();
  const m = dict[l as keyof typeof dict];
  if (!m) return undefined;
  const exact = m[text];
  if (exact) return exact;
  // Try case-insensitive exact match
  const lower = Object.entries(m).find(([k]) => k.toLowerCase() === text.toLowerCase());
  return lower?.[1];
}